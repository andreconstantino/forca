    private void btChutarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btChutarActionPerformed
        // TODO add your handling code here:
        char letra = tfLetra.getText().charAt(0);
        int tamanho = vetorPalavra.length;
        boolean contem = false;
        for(int i = 0; i<tamanho; i++){
            if(vetorPalavra[i] == letra){
                vetorMascara[i]=letra;
                contem = true;
            }            
        }
        if(!contem)
            erros++;
        lbErro.setText(erros+"/6");
        lbLetrasUsadas.setText(lbLetrasUsadas.getText() +letra+" ");
        lbPalavra.setText(new String(vetorMascara));
        tfLetra.setText("");
        verificarTermino();
        
    }//GEN-LAST:event_btChutarActionPerformed
    private void verificarTermino(){
        if(erros>=6){
            JOptionPane.showMessageDialog(rootPane,"Voce perdeu :(");
            disporPalavra();
            
        }
     String verificarPalavra = new String(vetorMascara);
     if(verificarPalavra.equals(palavra.getPalavra())){
         JOptionPane.showMessageDialog(rootPane,"Parabens,voce ganhou!");
         disporPalavra();
     }
    }
   
    

