    private void disporPalavra(){
        IControllerJogo cj = ControlerFactory.obterControlerJogo();
        palavra=cj.obterPalavra();
        lbDica.setText(palavra.getDica());
        int tamanho = palavra.getPalavra().length();
        vetorPalavra = new char[tamanho];
        vetorMascara = new char[tamanho];
        for(int i =0; i < tamanho; i++){
            vetorMascara[i] = '*';
            vetorPalavra[i] = palavra.getPalavra().charAt(i);
        }
        lbPalavra.setText(new String(vetorMascara));
        erros = 0;
        lbErro.setText(erros + "/6");
        this.lbLetrasUsadas.setText("");
    }
